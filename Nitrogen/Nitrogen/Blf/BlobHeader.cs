using Nitrogen.IO;
using System;
using System.Text;

namespace Nitrogen.Blf
{
    internal class BlobHeader
        : Chunk
    {
        private string fileSummary;
        private short byteOrderMark;

        internal BlobHeader()
            : base("_blf", 1, 48)
        {
            this.fileSummary = "Generated by Nitrogen";
            this.byteOrderMark = -2;
        }

        protected override ChunkFlags Flags { get { return ChunkFlags.IsHeader; } }

        protected override void SerializeEndianStreamData(IO.EndianStream s)
        {
            s.Stream(ref byteOrderMark);
            s.Stream(ref this.fileSummary, Encoding.ASCII, 32);

            if (this.byteOrderMark != -2)
                s.SetEndianness(ByteOrder.LittleEndian);
        }
    }
}
